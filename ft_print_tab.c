#include "libft.h"

void	ft_print_tab(char **tab)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (tab[i] != 0)
	{
		ft_putstr(tab[i]);
		ft_putchar('\n');
		i++;
	}
}
